#include "gpio.h"

/**
 * @brief  IO口初始化 
 * @param  gpio : IO口
 *         pin  : 引脚编号
 *         mode : 引脚模式
 **/
void GPIO_Init(GPIO_TypeDef * gpio , u8 pin , GPIOMode_TypeDef mode) 
{
    if(pin < 8)
		{
		    gpio -> CRL &= ~(0xF << (pin * 4)) ;  //! 清寄存器的值
			  gpio -> CRL |= mode << (pin * 4) ;    //! 配置参数
		}
		else
		{
			  pin -= 8 ;
		    gpio -> CRH &= ~(0xF << (pin * 4)) ;  //! 清寄存器的值
			  gpio -> CRH |= mode << (pin * 4) ;    //! 配置参数
		}
}
